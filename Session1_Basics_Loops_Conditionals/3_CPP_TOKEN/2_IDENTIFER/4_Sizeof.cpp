// about size of 
#include<iostream>
using namespace std;
int main(){
    int a;
    float c;
    char d;
    double s;
    bool j;
    long r;
    cout<<"Size of int data type is: "<<sizeof(a)<<endl;
    cout<<"Size of float data type is: "<<sizeof(c)<<endl;
    cout<<"Size of char data type is: "<<sizeof(d)<<endl;
    cout<<"Size of double data type is: "<<sizeof(s)<<endl;
    cout<<"Size of bool data type is: "<<sizeof(j)<<endl;
    cout<<"Size of long data type is: "<<sizeof(r)<<endl;
    return 0;
}